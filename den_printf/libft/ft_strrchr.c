#include <stddef.h>

char	*ft_strrchr(char const *s, int c)
{
	char const	*p;

	p = s;
	while (*p)
		++p;
	while (p != s - 1)
	{
		if (*p == (char)c)
			return ((char *)p);
		--p;
	}
	return (NULL);
}
