#include <stddef.h>

char	*ft_strchr(char const *s, int c)
{
	while (*s)
	{
		if (*s == (char)c)
			return ((char *)s);
		++s;
	}
	if (!(char)c)
		return ((char *)s);
	return (NULL);
}
