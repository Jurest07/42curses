#include <stddef.h>

void	*ft_memset(void *s, int c, size_t n)
{
	void	*p;

	p = s;
	while (n--)
	{
		*(char *)p = *(char *)&c;
		p = (void *)((char *)p + 1);
	}
	return (s);
}
